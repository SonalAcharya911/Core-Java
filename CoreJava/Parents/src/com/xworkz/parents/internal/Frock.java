package com.xworkz.parents.internal;

public class Frock {
    private String color;
    private String size;
    private String fabric;
    private boolean hasSleeves;

    public Frock(String color, String size, String fabric, boolean hasSleeves) {
        this.color = color;
        this.size = size;
        this.fabric = fabric;
        this.hasSleeves = hasSleeves;
    }
}
